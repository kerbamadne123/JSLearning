function monthOfYear(monthNumber) {
  switch (monthNumber) {
    case 1:
      console.log(`name of the Month ${monthNumber}: January`);
      break;

    case 2:
      console.log(`name of the Month ${monthNumber}: February`);

      break;
    case 3:
      console.log(`name of the Month ${monthNumber}: March`);
      break;
    case 4:
      console.log(`name of the Month ${monthNumber}: April`);
      break;
    case 5:
      console.log(`name of the Month ${monthNumber}: May`);
      break;
    case 6:
      console.log(`name of the Month ${monthNumber}: June`);
      break;
    case 7:
      console.log(`name of the Month ${monthNumber}: July`);
      break;
    case 8:
      console.log(`name of the Month ${monthNumber}: August`);
      break;
    case 9:
      console.log(`name of the Month ${monthNumber}: September`);
      break;
    case 10:
      console.log(`name of the Month ${monthNumber}: October`);
      break;
    case 11:
      console.log(`name of the Month ${monthNumber}: November`);
      break;
    case 12:
      console.log(`name of the Month ${monthNumber}: December`);
      break;
  }
}
monthOfYear();
monthOfYear(2);
monthOfYear(5);
monthOfYear(12);
monthOfYear(15);
monthOfYear(100);
